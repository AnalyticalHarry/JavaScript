// printing "Hello World" on the screen using console.log() function
console.log("Hello World!");

// create varaibles.
var x;
let y;

// storing value inside variables.
x = 5;
y = 6;
let z = x + y;
console.log(z)

// JavaScript syntax define two types of values.
// fixed values : It is called literals.
// varaibles values : It is called variables.

// example of literals. 
// numbers are written with or without decimals. 
10.50
1001
// string are text, within doubles or single quotes.
"Hello World!"
'Hello World'

// JavaScripts Variables. 
// Variables are used to store data values.
// keywords var, let, and const 
// equal sign is used to assign values to variables
let a, b, c;
a = 6;
b = 5; 
c = 10;
console.log(a + b + c);

// JavaScript Expressions
// a expression is a combination of values, variables, and operators, which compute to a value.
// the computation is called evaluation 
console.log(5 * 50);

let num = 50;
console.log(num * 70);

// the values can be of various types, such as numbers and strings.
console.log("Hemant" + " " + "Thapa")
